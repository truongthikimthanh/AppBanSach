    public void NhanVien_Load(){
           
        try {
            ps = conn.prepareStatement("SELECT * FROM NHACC ");
            rs = ps.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c = rsd.getColumnCount();
          
            DefaultTableModel model = (DefaultTableModel) listNCC.getModel();
            model.setRowCount(0);
            
            while (rs.next()){
                Vector v1 = new Vector();
                for(int i=1;i<=c;i++){
                    v1.add(rs.getString("MaNCC"));
                    v1.add(rs.getString("TenNCC"));
                    v1.add(rs.getString("SDT"));
                    v1.add(rs.getString("Email"));
                    v1.add(rs.getString("DChi"));                
              }
                model.addRow(v1);
               listNCC.setModel(model);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(FormNCC.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
